package app04_Prime_Number;

public class ForTestPattern {

	public static void main(String[] args) {
        int i,j;
        i=1;
        do {
        	j=1;
        	do {
        		System.out.println(j);
        		j++; 		
        	}while(j<=i);
        	System.out.println(i);
        	i++;
        }while(i<=5);
	}
        }
	

